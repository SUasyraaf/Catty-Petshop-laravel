    {
        //dd($request);

        //Method 1
        $booking =  new Booking();
        $booking->day_in = $request->get('checkin');
        $booking->day_out = $request->get('checkout');
        $booking->payment = $request->get('payment');
        $booking->save();

        return redirect()->route('booking:index');

